import java.io.PrintStream;





















































































































































































































































































































































































































































class AutoRobotControler
  extends Thread
{
  private Robot robot;
  private boolean stop;
  
  public AutoRobotControler(Robot robot)
  {
    this.robot = robot;
    stop = false;
    start();
  }
  
  public synchronized void setStop(boolean b) { stop = b; }
  public synchronized boolean getStop() { return stop; }
  
  private void autoLogic() {
    byte state = -1;
    Sensor f = new Sensor();
    Sensor l = new Sensor();
    Sensor r = new Sensor();
    
    while ((!robot.foundExit()) && (!getStop())) {
      robot.getSensors(f, l, r);
      if (!wall) {
        robot.turnRight();
        robot.step(RobotLabyrinth.stepsInBlock);
      } else if (!wall) {
        robot.step(RobotLabyrinth.stepsInBlock);
      } else {
        robot.turnLeft();
      }
      do {
        try {
          sleep(50L); } catch (Exception e) { System.out.println("Thread Sleep Error: " + e.toString()); }
        state = robot.getRobotState();
      } while (state != 1);
    }
  }
  

  public void run()
  {
    autoLogic();
  }
}
