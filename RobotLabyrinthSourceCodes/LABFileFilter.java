import java.io.File;
import javax.swing.filechooser.FileFilter;










































































































































































































































































































































































































































































































class LABFileFilter
  extends FileFilter
{
  LABFileFilter() {}
  
  public boolean accept(File f)
  {
    if (f.isDirectory()) return true;
    String fname = f.getName().toLowerCase();
    if (fname.endsWith(".lab")) return true;
    return false;
  }
  
  public String getDescription() { return new String("Labyrinth files (*.lab)"); }
}
